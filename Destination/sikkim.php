            Tea Garden: According to records, the first commercial tea gardens planted out by the British tea 
            interest were Tukvar, Steinthal and Aloobari tea estates. This was in 1852 and this entire plantation 
            used seeds that were raised in the government nurseries.<br><br>

            Darjeeling was then only a sparsely populated hamlet which was being used as a hill resort by the 
            army and some affluent people. Tea, being a labor intensive enterprise, required sufficient number 
            of workers to plant, tend, pluck and finally manufacture the produce. For this, employment was 
            offered to people from across the border of Nepal.<br><br>

            Tenzing Rock: Tenzing Rock, a gigantic natural rock in Darjeeling is named after Tenzing Norgay, the 
            man who created history by climbing the Everest first time along with Edmund Hillary in 1953.<br><br>

            Mall: Mall is like the town center or the social center of Darjeeling. This is where some of the old 
