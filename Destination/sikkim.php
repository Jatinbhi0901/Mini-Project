            Post breakfast embark on a sightseeing tour of Kalimpong covering - Mangal Dham, Deolo Hill, 
            Dr. Graham's Home, Durbin Dara Monastery & View point, Cactus / Orchid Nursery. Return to hotel. 
            Post lunch drive to to Gangtok - "The Capital of Sikkim". Check-in to your hotel on arrival. 
