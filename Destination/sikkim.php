            <br><br>
            Institute of Tibetology: Since its establishment in 1958, the Namgyal Institute of Tibetology has 
            sponsored and promoted research on the religion, history, language, art and culture of the people of 
            the Tibetan cultural area which includes Sikkim. The NIT library holds one of the largest collections 
            of Tibetan works in the world outside Tibet and a museum of Tibetan iconography and religious art. It 
            has published the Bulletin of Tibetology since 1964 and numerous books over the years.
            <br><br>
            Handicraft Center: The Directorate of Handicrafts and Handloom was founded in 1957, as Handloom and 
            Handicrafts Centre of Sikkim. This is situated 500 metres from the central market of Gangtok. This 
            organization has been established to conserve and pass around the art and craft skills of the 
            artificers of Gangtok. In this centre, the visitors can see blankets, elegant hand-carved folding 
            tables (Choktse), hand-loomed carpets with conventional themes, hand-painted masques, shawls, 
            traditional paintings, wooden dolls and many other handicrafts.
