            Mangal Dham: This ungainly modern Mangal Dham is sacred to Krishna. In its ballroom-like prayer hall 
            there are eight vibrant, life-size dioramas from the Krishna Leela. The temple is dedicated to Guruji 
            Shri Mangaldas Ji, who's commemorated in a shrine below the prayer hall. The temple is about 500m 
            downhill from Thongsa Gompa, or you can walk from the centre along Relli Rd and turn left by the 
            Roman Catholic church.
            <br><br>
            Deolo Hill: Deolo Hill, located to the north-east of the town, is one of the two hills in between 
            which the town of Kalimpong stands. From the top of this hill, tourists can enjoy beautiful views 
            of the surrounding villages of Relli valley and Teesta River along with the town of Kalimpong. In 
            addition, there is a park located at the summit of this hill, which is constructed for recreational 
            purposes.
